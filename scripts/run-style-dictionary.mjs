// CANONICAL token build — Style Dictionary v4 (JS API).
// Usage: npm run tokens   (or: node scripts/run-style-dictionary.mjs)
//
// Two-layer architecture (see DESIGN.md §0):
//   1. Corporate base (tokens/gsc.tokens.json) → all platforms:
//      build/{css,scss,js,ios,android}
//   2. Program overrides (tokens/programs/*.tokens.json) → scoped CSS only:
//      build/css/gsc-program-tokens.css, one [data-program="slug"] block each.
//
// Programs are built as SEPARATE Style Dictionary instances — merging them
// into one source is wrong (their token paths collide: 299 collisions).
// Program string metadata (program.id/name/voice/…) is filtered out of CSS;
// it is documentation, not a style value.
//
// Fallback without npm registry access: node scripts/build-tokens.mjs
import StyleDictionary from 'style-dictionary';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const rel = (p) => path.join(root, p);

/* ---------- 1 · Corporate base → all platforms --------------------------- */
const corporate = new StyleDictionary({
  source: [rel('tokens/gsc.tokens.json')],
  log: { verbosity: 'default' },
  platforms: {
    css: {
      transformGroup: 'css',
      buildPath: rel('build/css/') + path.sep,
      files: [{ destination: 'gsc-tokens.css', format: 'css/variables', options: { outputReferences: true } }]
    },
    scss: {
      transformGroup: 'scss',
      buildPath: rel('build/scss/') + path.sep,
      files: [{ destination: '_gsc-tokens.scss', format: 'scss/variables' }]
    },
    js: {
      transformGroup: 'js',
      buildPath: rel('build/js/') + path.sep,
      files: [
        { destination: 'gsc-tokens.js', format: 'javascript/es6' },
        { destination: 'gsc-tokens.json', format: 'json/flat' }
      ]
    },
    ios: {
      transformGroup: 'ios-swift',
      buildPath: rel('build/ios/') + path.sep,
      files: [{ destination: 'GSCTokens.swift', format: 'ios-swift/class.swift', options: { className: 'GSCTokens' } }]
    },
    android: {
      transformGroup: 'android',
      buildPath: rel('build/android/') + path.sep,
      files: [
        { destination: 'gsc_colors.xml', format: 'android/colors' },
        { destination: 'gsc_dimens.xml', format: 'android/dimens' }
      ]
    }
  }
});
await corporate.buildAllPlatforms();

/* ---------- 2 · Program overrides → scoped CSS ---------------------------- */
const progDir = rel('tokens/programs');
const programs = fs.readdirSync(progDir).filter(f => f.endsWith('.tokens.json')).sort();
const blocks = [];

for (const file of programs) {
  const slug = file.replace('.tokens.json', '');
  const sd = new StyleDictionary({
    source: [path.join(progDir, file)],
    log: { verbosity: 'silent' },
    platforms: {
      css: {
        transformGroup: 'css',
        buildPath: rel('build/css/') + path.sep,
        files: [{
          destination: `.tmp-${slug}.css`,
          format: 'css/variables',
          options: { selector: `[data-program="${slug}"]` },
          // Style values only — program.* metadata is docs, not CSS.
          filter: (token) => token.$type === 'color' || token.$type === 'fontFamily'
        }]
      }
    }
  });
  await sd.buildAllPlatforms();
  const tmp = rel(`build/css/.tmp-${slug}.css`);
  const css = fs.readFileSync(tmp, 'utf8')
    .replace(/\/\*\*[\s\S]*?\*\/\s*/, ''); // strip per-file "do not edit" banner
  fs.unlinkSync(tmp);
  blocks.push(css.trim());
  console.log(`✔︎ program: ${slug}`);
}

const header = '/* Program token scopes — generated by scripts/run-style-dictionary.mjs. Do not edit.\n   Canonical hand-authored CSS lives in css/programs.css; values must match. */\n\n';
fs.writeFileSync(rel('build/css/gsc-program-tokens.css'), header + blocks.join('\n\n') + '\n');
console.log(`✔︎ build/css/gsc-program-tokens.css (${programs.length} programs)`);
